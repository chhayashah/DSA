package stringss;

public class SP {
    public static void main(String[] args) {
        // String ki length print karo.
        String str = "chhaya";
        System.out.println(str.length());

        // First aur last character print karo.
        System.out.println(str.charAt(0));
        System.out.println(str.charAt(str.length() - 1));
    }
}
